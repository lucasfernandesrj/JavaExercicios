package com.lucasfernandes.cursojava.aula34;

public class ConversaoDeUnidadeDeArea {
	public static double converterMetroParaPes(double num1) {
		return num1*10.76;
	}
	
	public static double converterPesParaCentimetro(double num1) {
		return num1*929;
	}
	
	public static double converterMilhaParaAcres(double num1) {
		return num1*640;
	}
	
	public static double converterAcreParaPes(double num1) {
		return num1*43.560;
	}
}
