package com.lucasfernandes.cursojava.aula34;

public class ConversaoDeUnidadeDeVolume {
	public static double converterLitroParaCentCubico(double num1) {
		return num1*1000;
	}
	
	public static double converterMetroCubicoParaLitro(double num1) {
		return num1*1000;
	}
	
	public static double converterMetroCubicoParaPeCubico(double num1) {
		return num1*35.32;
	}
	
	public static double converterGalaoParaPolegadaCubica(double num1) {
		return num1*231;
	}
	
	public static double converterGalaoParaLitro(double num1) {
		return num1*3.785;
	}
}
