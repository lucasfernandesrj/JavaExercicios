package com.lucasfernandes.cursojava.aula34;

public class ConversaoDeUnidadesDeTempo {
	public static double converterMinutoParaSegundo(double num1) {
		return num1*60;
	}

	public static double converterHoraParaMinutos(double num1) {
		return num1*60;
	}
	
	public static double converterDiaParHora(double num1) {
		return num1*24;
	}
	
	public static double converterSemanaParaDia(double num1) {
		return num1*7;
	}
	
	public static double converterMesParaDia(double num1) {
		return num1*30;
	}
	
	public static double converterAnoParaDia(double num1) {
		return num1*365.25;
	}
	
}
